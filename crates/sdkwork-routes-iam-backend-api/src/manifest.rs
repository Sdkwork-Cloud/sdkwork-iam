fn enrich_backend_route(route: HttpRoute) -> HttpRoute {
    if route.required_permission.is_some() {
        return route;
    }
    HttpRoute {
        required_permission: iam_backend_permission_for_operation(route.operation_id),
        ..route
    }
}

static IAM_BACKEND_ENRICHED_ROUTES: std::sync::OnceLock<&'static [HttpRoute]> =
    std::sync::OnceLock::new();

fn enriched_backend_routes() -> &'static [HttpRoute] {
    IAM_BACKEND_ENRICHED_ROUTES.get_or_init(|| {
        let routes: Vec<HttpRoute> = IAM_BACKEND_API_ROUTES
            .iter()
            .copied()
            .map(enrich_backend_route)
            .collect();
        Box::leak(routes.into_boxed_slice())
    })
}

pub fn backend_routes() -> Vec<HttpRoute> {
    IAM_BACKEND_API_ROUTES.to_vec()
}

pub fn iam_backend_enriched_routes() -> &'static [HttpRoute] {
    enriched_backend_routes()
}

pub fn iam_backend_api_route_manifest() -> HttpRouteManifest {
    HttpRouteManifest::new(enriched_backend_routes())
}

pub fn sdkwork_iam_backend_api_routes() -> Vec<HttpRoute> {
    iam_backend_enriched_routes().to_vec()
}
